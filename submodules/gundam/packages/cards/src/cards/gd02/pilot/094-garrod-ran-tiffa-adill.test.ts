/**
 * Garrod Ran & Tiffa Adill (GD02-094)
 *
 * 【When Paired】You may discard 1. If you do, look at the top 3 cards
 * of your deck. …
 *
 * Exercises the generic `dependsOnPrevious` primitive on an **optional
 * non-targeted** predecessor (discard).
 */

import { describe, expect, it } from "vite-plus/test";
import type { CardEffect } from "@tcg/gundam-types";
import { GundamTestEngine, PLAYER_ONE, createMockUnit, expectSuccess } from "@tcg/gundam-engine";
import { gd02GarrodRanTiffaAdill094 } from "./094-garrod-ran-tiffa-adill.ts";

describe("Garrod Ran & Tiffa Adill (GD02-094)", () => {
  // effect[1] is the 【When Paired】 trigger; effect[0] is the Burst.
  const pairEffect = gd02GarrodRanTiffaAdill094.effects![1] as CardEffect;

  it("discards + runs the tutor when controller opts into the optional", () => {
    // Seed the top of deck with a known (Vulture) Unit card plus two
    // non-matches so we can observe the `lookAtTopDeck` tutor moving the
    // matching card into hand. Rest of the deck stays empty-shaped so
    // only the 3 seeded cards are in the top-3 reveal window.
    const vultureUnit = createMockUnit({ traits: ["vulture"] });
    const nonMatch1 = createMockUnit({ traits: [] });
    const nonMatch2 = createMockUnit({ traits: ["zeon"] });
    const engine = GundamTestEngine.create(
      {
        hand: [createMockUnit(), createMockUnit(), createMockUnit()],
        deck: [vultureUnit, nonMatch1, nonMatch2],
      },
      {},
    );
    const p1 = engine.asPlayer(PLAYER_ONE);
    const handBefore = p1.getHand().length;
    const trashBefore = p1.getCardsInZone("trash").length;

    engine.getG().pendingEffects.push({
      id: "garrod_1",
      sourceCardId: "src",
      effectIndex: 1,
      kind: "triggered",
      effect: pairEffect,
      controllerId: PLAYER_ONE,
    });

    expectSuccess(engine.asPlayer(PLAYER_ONE).resolveEffect({ optionalAnswers: { 0: true } }));

    // Discard ran → trash +1 (one of the 3 hand mocks was discarded).
    expect(p1.getCardsInZone("trash").length).toBe(trashBefore + 1);

    // lookAtTopDeck tutored the seeded Vulture Unit into hand. Net hand
    // delta: −1 (discard) + 1 (tutor) = 0 relative to `handBefore`.
    const handAfter = p1.getHand();
    expect(handAfter.length).toBe(handBefore);
    const vultureInHand = p1.getCardsInZone("hand").some((instanceId) => {
      // instanceIds generated by GundamTestEngine embed the cardNumber
      // as `${playerId}_${cardNumber}_${n}` — match on that suffix.
      return instanceId.includes(`_${vultureUnit.cardNumber}_`);
    });
    expect(vultureInHand).toBe(true);
  });

  it("skips the tutor when controller declines the optional", () => {
    const engine = GundamTestEngine.create(
      { hand: [createMockUnit(), createMockUnit(), createMockUnit()], deck: 5 },
      {},
    );
    const p1 = engine.asPlayer(PLAYER_ONE);
    const handBefore = p1.getHand().length;
    const deckBefore = engine.getCardCount({ zone: "deck", playerId: PLAYER_ONE });
    const trashBefore = p1.getCardsInZone("trash").length;

    engine.getG().pendingEffects.push({
      id: "garrod_2",
      sourceCardId: "src",
      effectIndex: 1,
      kind: "triggered",
      effect: pairEffect,
      controllerId: PLAYER_ONE,
    });

    expectSuccess(engine.asPlayer(PLAYER_ONE).resolveEffect({ optionalAnswers: { 0: false } }));

    // Nothing happened: no discard, no deck manipulation.
    expect(p1.getHand().length).toBe(handBefore);
    expect(engine.getCardCount({ zone: "deck", playerId: PLAYER_ONE })).toBe(deckBefore);
    expect(p1.getCardsInZone("trash").length).toBe(trashBefore);
  });
});
